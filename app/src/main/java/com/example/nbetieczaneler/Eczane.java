package com.example.nbetieczaneler;

public class Eczane {
    private String eczaneAdi;
    private String telefonNumari;
    private String adres;

    public String getEczaneAdi() {
        return eczaneAdi;
    }

    public void setEczaneAdi(String eczaneAdi) {
        this.eczaneAdi = eczaneAdi;
    }

    public String getTelefonNumari() {
        return telefonNumari;
    }

    public void setTelefonNumari(String telefonNumari) {
        this.telefonNumari = telefonNumari;
    }

    public String getAdres() {
        return adres;
    }

    public void setAdres(String adres) {
        this.adres = adres;
    }
}